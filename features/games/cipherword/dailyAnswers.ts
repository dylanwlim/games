export type CipherwordDailyAnswer = (typeof CIPHERWORD_DAILY_ANSWERS)[number];

const ISO_DATE_RE = /^\d{4}-\d{2}-\d{2}$/;

export type CipherwordArchiveStatus = "available" | "today" | "locked";

export type CipherwordArchiveEntry = {
  date: string;
  index: number;
  puzzleId: string;
  status: CipherwordArchiveStatus;
  answer?: CipherwordDailyAnswer;
};

export function assertIsoDate(value: string): void {
  if (!ISO_DATE_RE.test(value)) throw new Error(`Invalid ISO date: ${value}`);
}

export function utcOrdinalFromIsoDate(value: string): number {
  assertIsoDate(value);
  const [year, month, day] = value.split("-").map(Number);
  return Math.floor(Date.UTC(year, month - 1, day) / MS_PER_DAY);
}

export function isoDateFromUtcOrdinal(ordinal: number): string {
  return new Date(ordinal * MS_PER_DAY).toISOString().slice(0, 10);
}

export function addDaysToIsoDate(value: string, days: number): string {
  return isoDateFromUtcOrdinal(utcOrdinalFromIsoDate(value) + days);
}

export function daysInclusive(startDate: string, endDate: string): number {
  return utcOrdinalFromIsoDate(endDate) - utcOrdinalFromIsoDate(startDate) + 1;
}

export function tryGetCipherwordDailyIndex(dateIso: string): number | null {
  const index = utcOrdinalFromIsoDate(dateIso) - utcOrdinalFromIsoDate(CIPHERWORD_DAILY_START_DATE);
  if (index < 0 || index >= CIPHERWORD_DAILY_ANSWERS.length) return null;
  return index;
}

export function isCipherwordDailyDateInRange(dateIso: string): boolean {
  return tryGetCipherwordDailyIndex(dateIso) !== null;
}

export function getCipherwordDailyIndex(dateIso: string): number {
  const index = tryGetCipherwordDailyIndex(dateIso);
  if (index === null) throw new RangeError(`Cipherword date out of range: ${dateIso}`);
  return index;
}

export function getCipherwordDateForIndex(index: number): string {
  if (!Number.isInteger(index) || index < 0 || index >= CIPHERWORD_DAILY_ANSWERS.length) {
    throw new RangeError(`Cipherword index out of range: ${index}`);
  }
  return addDaysToIsoDate(CIPHERWORD_DAILY_START_DATE, index);
}

export function getCipherwordDailyAnswerForDate(dateIso: string): CipherwordDailyAnswer | null {
  const index = tryGetCipherwordDailyIndex(dateIso);
  return index === null ? null : CIPHERWORD_DAILY_ANSWERS[index];
}

export function getRequiredCipherwordDailyAnswerForDate(dateIso: string): CipherwordDailyAnswer {
  return CIPHERWORD_DAILY_ANSWERS[getCipherwordDailyIndex(dateIso)];
}

export function getCanonicalCipherwordDate(now: Date = new Date()): string {
  const parts = new Intl.DateTimeFormat("en-CA", {
    timeZone: CIPHERWORD_CANONICAL_TIME_ZONE,
    year: "numeric",
    month: "2-digit",
    day: "2-digit",
  }).formatToParts(now);
  const get = (type: string) => parts.find((part) => part.type === type)?.value;
  return `${get("year")}-${get("month")}-${get("day")}`;
}

export function getTodayCipherwordDailyAnswer(
  now: Date = new Date(),
): CipherwordDailyAnswer | null {
  return getCipherwordDailyAnswerForDate(getCanonicalCipherwordDate(now));
}

export function getCipherwordPuzzleIdForDate(dateIso: string): string {
  return `cipherword-daily-${dateIso}`;
}

export function getCipherwordArchiveEntry(
  dateIso: string,
  now: Date = new Date(),
  includeAnswer = false,
): CipherwordArchiveEntry {
  const todayIso = getCanonicalCipherwordDate(now);
  const start = utcOrdinalFromIsoDate(CIPHERWORD_DAILY_START_DATE);
  const end = utcOrdinalFromIsoDate(CIPHERWORD_DAILY_END_DATE);
  const ordinal = utcOrdinalFromIsoDate(dateIso);
  if (ordinal < start || ordinal > end)
    throw new RangeError(`Cipherword archive date out of range: ${dateIso}`);
  const todayOrdinal = utcOrdinalFromIsoDate(todayIso);
  const status: CipherwordArchiveStatus =
    ordinal > todayOrdinal ? "locked" : ordinal === todayOrdinal ? "today" : "available";
  const index = ordinal - start;
  return {
    date: dateIso,
    index,
    puzzleId: getCipherwordPuzzleIdForDate(dateIso),
    status,
    ...(includeAnswer && status !== "locked" ? { answer: CIPHERWORD_DAILY_ANSWERS[index] } : {}),
  };
}

export function getCipherwordArchiveRange(
  startDate: string,
  endDate: string,
  now: Date = new Date(),
  includeAnswers = false,
): CipherwordArchiveEntry[] {
  const start = utcOrdinalFromIsoDate(startDate);
  const end = utcOrdinalFromIsoDate(endDate);
  if (end < start) return [];
  return Array.from({ length: end - start + 1 }, (_, offset) =>
    getCipherwordArchiveEntry(isoDateFromUtcOrdinal(start + offset), now, includeAnswers),
  );
}

export function validateCipherwordDailyBank(): {
  ok: true;
  count: number;
  startDate: string;
  endDate: string;
} {
  const expected = daysInclusive(CIPHERWORD_DAILY_START_DATE, CIPHERWORD_DAILY_END_DATE);
  if (expected !== CIPHERWORD_DAILY_REQUIRED_COUNT)
    throw new Error(`Date coverage mismatch: expected ${expected}`);
  if (CIPHERWORD_DAILY_ANSWERS.length !== expected)
    throw new Error(`Answer count mismatch: ${CIPHERWORD_DAILY_ANSWERS.length} !== ${expected}`);
  const seen = new Set<string>();
  for (const answer of CIPHERWORD_DAILY_ANSWERS) {
    if (!/^[a-z]{4,10}$/.test(answer)) throw new Error(`Invalid answer: ${answer}`);
    if (seen.has(answer)) throw new Error(`Duplicate answer: ${answer}`);
    seen.add(answer);
  }
  return {
    ok: true,
    count: CIPHERWORD_DAILY_ANSWERS.length,
    startDate: CIPHERWORD_DAILY_START_DATE,
    endDate: CIPHERWORD_DAILY_END_DATE,
  };
}

export const CIPHERWORD_DAILY_BANK_VALIDATION = validateCipherwordDailyBank();
